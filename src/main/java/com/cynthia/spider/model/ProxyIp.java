package com.cynthia.spider.model;

public class ProxyIp {

	//代理IP地址
	String address;
	//代理服务器端口
	int port;
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public int getPort() {
		return port;
	}
	public void setPort(int port) {
		this.port = port;
	}
	
}
